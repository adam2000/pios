#ifndef BakingPi_bptypes_h
#define BakingPi_bptypes_h

/*!
 *  @brief Boolean type as per C99
 */
typedef _Bool bool;
#define	false	0
#define	true	1

/*!
 *  @brief unsigned 8 bit type as per C99
 */
typedef unsigned char uint8_t;
/*!
 *  @brief uint32_t as per C99
 */
typedef unsigned int uint32_t;

#define NULL	((void*) 0)

#endif
